/**
 * defines server assembly metadata
 *
 * @export
 * @interface AssemblyInfo
 */
export interface AssemblyInfo {

    /**
     * Assembly Title
     *
     * @type {string}
     * @memberof AssemblyInfo
     */
    assemblyTitle: string;

    /**
     * Assembly Version
     *
     * @type {string}
     * @memberof AssemblyInfo
     */
    assemblyVersion: string;

    /**
     * Assembly Version Detail
     *
     * @type {string}
     * @memberof AssemblyInfo
     */
    assemblyVersionDetail: string;

    /**
     * Assembly Description
     *
     * @type {string}
     * @memberof AssemblyInfo
     */
    assemblyDescription: string;

    /**
     * Assembly Product
     *
     * @type {string}
     * @memberof AssemblyInfo
     */
    assemblyProduct: string;

    /**
     * Assembly Copyright
     *
     * @type {string}
     * @memberof AssemblyInfo
     */
    assemblyCopyright: string;

    /**
     * Assembly Company
     *
     * @type {string}
     * @memberof AssemblyInfo
     */
    assemblyCompany: string;
}
